		</p>

		<p class="make-ms1g2_text">
			Вращатель оборудования со шнеком перед вибропогружением устанавливается в зажимное устройство вибропогружателя
			с помощью крана-манипулятора и монтажного стола и производится бурение скважины, после чего оборудование снимается
