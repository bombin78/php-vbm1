		<a class="make-ms1g2_img-link" href="<?php echo $absAddress; ?>images/make-ms1g2/big/drilling-equipment.jpg" title="Буровое оборудование для устройства лидерных скважин">
			<img class="make-ms1g2_img" alt="Буровое оборудование" src="<?php echo $absAddress; ?>images/make-ms1g2/drilling-equipment.jpg">
		</a>

		<p class="make-ms1g2_text">
			Оборудование к машинам МС предназначено для предварительного бурения скважин диаметром 350 мм
			перед вибропогружением свайных фундаментов ТАС, ТСА, ТСС, ФК, ФКА, ТСП, ТСПЭ опор контактной сети и
			свай сечением 35х35см в сложных геологических условиях (сезонная мерзлота, наличие включений и пр.)
			при обустройстве и ремонте контактной сети железных дорог Российской федерации
			при температуре окружающего воздуха от -25 С до +40 С.
		</p>

		<p class="make-ms1g2_text">
			В состав оборудования входят: вращатель, шнек с резцовой головкой, люнет и комплекты монтажных частей и принадлежностей.
		</p>

		<p class="make-ms1g2_text">
			Отличительной особенностью вращателя является то, что в качестве приводного двигателя используется высокомоментный гидромотор,
			обеспечивающий надежность, компактность конструкции и снижение эксплуатационных расходов.
		</p>

		<p class="make-ms1g2_text">
			Вращатель оборудования со шнеком перед вибропогружением устанавливается в зажимное устройство вибропогружателя
			с помощью крана-манипулятора и монтажного стола и производится бурение скважины, после чего оборудование снимается
			с помощью крана и осуществляется погружение фундамента.
		</p>

		<p class="make-ms1g2_text">
			Для обеспечения вертикальности бурения служит специальный люнет, пропускающий в раскрытом положении вибропогружатель,
			что дает возможность погружать фундаменты без его демонтажа.
		</p>

	</div>

