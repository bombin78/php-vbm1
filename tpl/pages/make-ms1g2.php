		<p class="make-ms1g2_text">
			Пневмоударное оборудование к машинам МС предназначено для бурения скважин  400 мм в скальных грунтах
			I и II группы по СТН ЦЭ 141-99 под фундаменты ФБС опор контактной сети и анкеры АБС
			при электрификации железных дорог Российской Федерации при температуре окружающего воздуха от -25 С до +40 С.
		</p>

		<p class="make-ms1g2_text">
			Оборудование изготавливается в двух вариантах:
			1 вариант – для установки в зажимное устройство вибропогружателя;
			2 вариант – для установки на кронштейне вместо вибропогружателя.
			Комплекты поставки по обоим вариантам отличаются наличием кронштейна для крепления вращателя.
		</p>

		<p class="make-ms1g2_text">
			В состав оборудования входят:
			вариант 1 – вращатель, воздушный вертлюг, штанга, пневмоударник с долотом, кожух пневмоударника, люнет, комплекты монтажных частей, инструмента и приспособлений;
			вариант 2 – кронштейн, воздушный вертлюг, штанга, пневмоударник с долотом, кожух пневмоударника, люнет, комплекты монтажных частей, инструмента и приспособлений.
		</p>

		<p class="make-ms1g2_text">
			1 вариант рекомендуется применять при малом объеме буровых работ. Он не требует демонтажа вибропогружателя,
			но затрудняет регулировку усилия и скорости подачи из-за большой массы вибропогружателя с грузовой кареткой.
		</p>

		<p class="make-ms1g2_text">
			Для обеспечения вертикальности бурения служит специальный люнет, пропускающий в раскрытом положении вибропогружатель,
			что дает возможность погружать фундаменты без его демонтажа.
		</p>

		<p class="make-ms1g2_text">
			При больших объемах бурения желательно применять оборудование по 2 варианту так, как значительно снижается масса навешенного оборудования
			и разгружаются металлоконструкции машины, также упрощается настройка гидросистемы.
		</p>

		<p class="make-ms1g2_text">
			Для эффективной работы оборудования должна обеспечиваться подача 45…50 м³/мин воздуха при давлении на входе в пневмоударник не менее 12 кг/см².
		</p>

		<p class="make-ms1g2_text">
			Пневматическое оборудование, включающее компрессорные станции, обратные клапаны, разобщительные краны, лубрикатор (автоматическую масленку),
			воздушные рукава и детали их соединения, а также оборудование для пылеотвода в основной комплект поставки не входят и могут быть заказаны отдельно.
		</p>

